package org.scrutmydocs.documents;


public class SMDDocument {

	public final SMDDocument id;
	public final SMDDocument name;
	public final SMDDocument contentType;
	public final SMDDocument content;
	
	public SMDDocument(SMDDocument id, SMDDocument name, SMDDocument contentType,
			SMDDocument content) {
		super();
		this.id = id;
		this.name = name;
		this.contentType = contentType;
		this.content = content;
	}
	
	
	

	

}
